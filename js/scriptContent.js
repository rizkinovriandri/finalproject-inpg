let comments = [];
let names = [];

function onSubmit(event) {
    event.preventDefault();

    //get data dari input box user
    let nama = document.getElementById('txtNama').value;
    let komentar = document.getElementById('txtKomentar').value;

    if(nama === ""){
        alert("Harap isi nama anda!");
    } else if (komentar === ""){
        alert("Harap isi komentar anda!");
    } else {

        comments.unshift(komentar);
        names.unshift(nama);

        let data = '';

        for(i = 0 ; i<comments.length ; i++){
            data += '<li class="list-group-item"><b>' + names[i] + '</b><br><br>'+ comments[i] + '</li>';
        }

        var jlhKomentar = comments.length;
        
        var data2 = "(" + jlhKomentar + ")";

        document.getElementById("listComments").innerHTML = data;
        document.getElementById("countComment").innerHTML = data2;

        console.log("data");
        //alert("Terima kasih telah memberikan komentar");

        document.getElementById('txtNama').value = "";
        document.getElementById('txtKomentar').value = "";
        document.getElementById('txtNama').focus();
        //kirim balik data dari js ke html
    }
    

}



